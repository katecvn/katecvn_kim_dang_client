import { Checkbox } from '@/components/ui/checkbox'
import { Badge } from '@/components/ui/badge'
import { Button } from '@/components/custom/Button'
import {
  Select,
  SelectContent,
  SelectItem,
  SelectTrigger,
  SelectValue,
} from '@/components/ui/select'
import { moneyFormat } from '@/utils/money-format'
import { dateFormat } from '@/utils/date-format'
import { cn } from '@/lib/utils'
import { ChevronDown, MoreVertical, Eye, Trash2 } from 'lucide-react'
import { useState } from 'react'
import {
  DropdownMenu,
  DropdownMenuContent,
  DropdownMenuItem,
  DropdownMenuSeparator,
  DropdownMenuTrigger,
} from '@/components/ui/dropdown-menu'
import { paymentStatus } from '../data'
import { useMemo } from 'react'
import { useDispatch } from 'react-redux'
import { updatePaymentStatus, getPayments } from '@/stores/PaymentSlice'
import { toast } from 'sonner'
import UpdatePaymentStatusDialog from './UpdatePaymentStatusDialog'
import { DeletePaymentDialog } from './DeletePaymentDialog'
import ViewPaymentDialog from './ViewPaymentDialog'

const MobilePaymentCard = ({
  payment,
  isSelected,
  onSelectChange,
}) => {
  const dispatch = useDispatch()
  const [expanded, setExpanded] = useState(false)
  const [showViewDialog, setShowViewDialog] = useState(false)
  const [showUpdateStatusDialog, setShowUpdateStatusDialog] = useState(false)
  const [showDeleteDialog, setShowDeleteDialog] = useState(false)
  const [targetStatus, setTargetStatus] = useState(null)

  const { code, paymentDate, createdAt, amount, status, reason, paymentMethod, receiverType, purchaseOrder } = payment

  const handleUpdateStatus = async (newStatus, id) => {
    try {
      await dispatch(updatePaymentStatus({ id, status: newStatus })).unwrap()
      toast.success('Cập nhật trạng thái thành công')
      setShowUpdateStatusDialog(false)
      dispatch(getPayments({}))
    } catch (error) {
      console.error(error)
      toast.error('Cập nhật trạng thái thất bại')
    }
  }


  // Filter statuses logic matching UpdatePaymentStatusDialog
  const filteredStatuses = useMemo(() => {
    const normalizedStatus = status === 'cancelled' ? 'cancelled' : status

    if (normalizedStatus === 'cancelled') {
      return paymentStatus.filter(
        (s) => s.value === 'cancelled',
      )
    }
    if (normalizedStatus === 'completed') {
      return paymentStatus.filter((s) => s.value !== 'draft')
    }
    return paymentStatus
  }, [status])

  const selectedStatusObj = paymentStatus.find((s) => s.value === status)

  const handleStatusChange = (newStatus) => {
    setTargetStatus(newStatus)
    setShowUpdateStatusDialog(true)
  }

  const getStatusBadge = (statusValue) => {
    const statusObj = paymentStatus.find((s) => s.value === statusValue)
    const colorClass = statusValue === 'completed' ? 'bg-green-500' : statusValue === 'cancelled' ? 'bg-red-500' : 'bg-yellow-500'

    return (
      <Badge
        className={`cursor-pointer hover:underline ${colorClass}`}
        onClick={() => setShowUpdateStatusDialog(true)}
      >
        {statusObj?.label || statusValue}
      </Badge>
    )
  }

  return (
    <>
      {showViewDialog && (
        <ViewPaymentDialog
          open={showViewDialog}
          onOpenChange={setShowViewDialog}
          paymentId={payment.id}
          showTrigger={false}
        />
      )}

      {showUpdateStatusDialog && (
        <UpdatePaymentStatusDialog
          open={showUpdateStatusDialog}
          onOpenChange={(open) => {
            setShowUpdateStatusDialog(open)
            if (!open) setTargetStatus(null)
          }}
          paymentId={payment.id}
          currentStatus={status}
          targetStatus={targetStatus}
          statuses={paymentStatus}
          onSubmit={handleUpdateStatus}
          contentClassName="z-[10002]"
          overlayClassName="z-[10001]"
        />
      )}

      {showDeleteDialog && (
        <DeletePaymentDialog
          open={showDeleteDialog}
          onOpenChange={setShowDeleteDialog}
          payment={payment}
          showTrigger={false}
        />
      )}

      <div className="border rounded-lg bg-card mb-3 overflow-hidden">
        {/* Header */}
        <div className="p-3 border-b bg-background/50 flex items-center gap-2">
          <Checkbox
            checked={isSelected}
            onCheckedChange={onSelectChange}
            className="h-4 w-4"
          />

          <div className="flex-1 min-w-0" onClick={() => setShowViewDialog(true)}>
            <div
              className="font-semibold text-sm truncate text-primary cursor-pointer hover:underline"
            >
              <div className="flex items-center gap-2">
                <span>{code}</span>
              </div>
            </div>
            <div className="text-xs text-muted-foreground">{dateFormat(paymentDate || createdAt)}</div>
          </div>

          <Button
            variant="ghost"
            size="sm"
            className="h-8 w-8 p-0"
            onClick={() => setExpanded(!expanded)}
          >
            <ChevronDown className={cn('h-4 w-4 transition-transform', expanded && 'rotate-180')} />
          </Button>

          <DropdownMenu>
            <DropdownMenuTrigger asChild>
              <Button variant="ghost" size="sm" className="h-8 w-8 p-0">
                <MoreVertical className="h-4 w-4" />
              </Button>
            </DropdownMenuTrigger>
            <DropdownMenuContent align="end" className="w-40">
              <DropdownMenuItem onClick={() => setShowViewDialog(true)}>
                <Eye className="mr-2 h-4 w-4" />
                Xem
              </DropdownMenuItem>
              {(status === 'draft' || status === 'cancelled') && (
                <>
                  <DropdownMenuSeparator />
                  <DropdownMenuItem
                    onClick={() => setShowDeleteDialog(true)}
                    className="text-destructive focus:text-destructive"
                  >
                    <Trash2 className="mr-2 h-4 w-4" />
                    Xóa
                  </DropdownMenuItem>
                </>
              )}
            </DropdownMenuContent>
          </DropdownMenu>
        </div>

        {/* Amount & Status Section */}
        <div className="p-3 border-b bg-background/30 space-y-2">
          <div className="flex justify-between items-center">
            <span className="text-xs text-muted-foreground">Số tiền:</span>
            <span className="text-sm font-semibold text-red-600">{moneyFormat(amount)}</span>
          </div>

          <div className="flex justify-between items-center">
            <span className="text-xs text-muted-foreground">Trạng thái:</span>
            {/* Replaced Badge with Select */}
            <div className="w-[140px]">
              <Select
                value={status}
                onValueChange={handleStatusChange}
              >
                <SelectTrigger className="h-7 text-xs px-2">
                  <SelectValue placeholder="Chọn trạng thái">
                    {selectedStatusObj ? (
                      <span
                        className={`inline-flex items-center gap-1 font-medium ${selectedStatusObj.value === 'completed' ? 'text-green-600' : selectedStatusObj.value === 'cancelled' ? 'text-red-600' : 'text-yellow-600'
                          }`}
                      >
                        {selectedStatusObj.label}
                      </span>
                    ) : null}
                  </SelectValue>
                </SelectTrigger>
                <SelectContent position="popper" align="end" className="w-[140px] z-[10005]">
                  {filteredStatuses.map((s) => (
                    <SelectItem
                      key={s.value}
                      value={s.value}
                      className="cursor-pointer text-xs"
                    >
                      <span
                        className={`inline-flex items-center gap-1 font-medium ${s.value === 'completed' ? 'text-green-600' : s.value === 'cancelled' ? 'text-red-600' : 'text-yellow-600'
                          }`}
                      >
                        {s.label}
                      </span>
                    </SelectItem>
                  ))}
                </SelectContent>
              </Select>
            </div>
          </div>
        </div>

        {/* Info Section */}
        <div className="p-3 border-b bg-background/30 space-y-1.5">
          <div className="flex justify-between items-start">
            <span className="text-xs text-muted-foreground w-20 flex-shrink-0">Người nhận:</span>
            <span className="text-xs text-right font-medium">
              {receiverType === 'customer' ? 'Khách hàng' :
                receiverType === 'supplier' ? 'Nhà cung cấp' :
                  receiverType || 'Khác'}
            </span>
          </div>
          <div className="flex justify-between items-start">
            <span className="text-xs text-muted-foreground w-20 flex-shrink-0">Thanh toán:</span>
            <Badge variant="outline" className="text-[10px] h-5 px-1 font-normal">
              {paymentMethod === 'cash' ? 'Tiền mặt' : (paymentMethod === 'transfer' || paymentMethod === 'bank_transfer') ? 'Chuyển khoản' : paymentMethod}
            </Badge>
          </div>
        </div>

        {/* Expanded Details */}
        {expanded && (
          <div className="p-3 bg-muted/30 space-y-2 border-t text-xs">
            {purchaseOrder && (
              <div className="flex justify-between">
                <span className="text-muted-foreground">Đơn mua:</span>
                <span className="font-medium">{purchaseOrder.code}</span>
              </div>
            )}

            <div className="flex flex-col gap-1">
              <span className="text-muted-foreground">Lý do:</span>
              <span className="italic">{reason || 'Không có'}</span>
            </div>
          </div>
        )}
      </div>
    </>
  )
}

export default MobilePaymentCard
