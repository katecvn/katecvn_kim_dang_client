import api from '@/utils/axios'
import { handleError } from '@/utils/handle-error'
import { createSlice, createAsyncThunk } from '@reduxjs/toolkit'
import { toast } from 'sonner'

export const getTaxes = createAsyncThunk(
  'tax',
  async (_, { rejectWithValue }) => {
    try {
      const response = await api.get('/tax')
      const { data } = response.data
      return data
    } catch (error) {
      const message = handleError(error)
      return rejectWithValue(message)
    }
  },
)

export const deleteTax = createAsyncThunk(
  'tax/delete',
  async (data, { rejectWithValue, dispatch }) => {
    try {
      await api.delete(`/tax/${data}/delete`)
      await dispatch(getTaxes()).unwrap()
      toast.success('Xóa thành công')
    } catch (error) {
      const message = handleError(error)
      return rejectWithValue(message)
    }
  },
)

export const createTax = createAsyncThunk(
  'tax/create',
  async (data, { rejectWithValue, dispatch }) => {
    try {
      await api.post('/tax/create', data)

      await dispatch(getTaxes()).unwrap()
      toast.success('Thêm mới thành công')
    } catch (error) {
      const message = handleError(error)
      return rejectWithValue(message)
    }
  },
)

export const updateTax = createAsyncThunk(
  'tax/update',
  async (updateData, { rejectWithValue, dispatch }) => {
    try {
      const { id, data } = updateData
      await api.put(`/tax/${id}/update`, data)
      await dispatch(getTaxes()).unwrap()
      toast.success('Cập nhật dữ liệu thành công')
    } catch (error) {
      const message = handleError(error)
      return rejectWithValue(message)
    }
  },
)

const initialState = {
  taxes: [],
  loading: false,
  error: null,
}

export const taxSlice = createSlice({
  name: 'tax',
  initialState,
  reducers: {},
  extraReducers: (builder) => {
    builder
      .addCase(getTaxes.pending, (state) => {
        state.loading = true
        state.error = null
      })
      .addCase(getTaxes.fulfilled, (state, action) => {
        state.loading = false
        state.taxes = action.payload
      })
      .addCase(getTaxes.rejected, (state, action) => {
        state.loading = false
        state.error = action.payload.message || 'Lỗi không xác định'
        toast.error(state.error)
      })
      .addCase(deleteTax.fulfilled, (state) => {
        state.loading = false
      })
      .addCase(deleteTax.rejected, (state, action) => {
        state.loading = false
        state.error = action.payload.message || 'Lỗi không xác định'
        toast.error(state.error)
      })
      .addCase(deleteTax.pending, (state) => {
        state.loading = true
        state.error = null
      })
      .addCase(createTax.pending, (state) => {
        state.loading = true
        state.error = null
      })
      .addCase(createTax.fulfilled, (state) => {
        state.loading = false
      })
      .addCase(createTax.rejected, (state, action) => {
        state.loading = false
        state.error = action.payload.message || 'Lỗi không xác định'
        toast.error(state.error)
      })
      .addCase(updateTax.pending, (state) => {
        state.loading = true
        state.error = null
      })
      .addCase(updateTax.fulfilled, (state) => {
        state.loading = false
      })
      .addCase(updateTax.rejected, (state, action) => {
        state.loading = false
        state.error = action.payload.message || 'Lỗi không xác định'
        toast.error(state.error)
      })
  },
})

export default taxSlice.reducer
